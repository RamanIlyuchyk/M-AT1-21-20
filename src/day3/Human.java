package day3;

public class Human {
    private int age;
    private boolean abilityToSpeak;

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public boolean isAbilityToSpeak() {
        return abilityToSpeak;
    }

    public void setAbilityToSpeak(boolean abilityToSpeak) {
        this.abilityToSpeak = abilityToSpeak;
    }
}